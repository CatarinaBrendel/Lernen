a = raw_input("Enter hier something: > ")
print ("{} is {}".format(a, type(a)))
print ("Is {} UPPERCASE? {}".format(a, a.isupper()))
print ("Is {} lowercase? {}".format(a, a.islower()))
print ("Is {} alphabetic? {}".format(a, a.isalpha()))
print ("Is {} title? {}".format(a, a.istitle()))
print ("Is {} alphanumeric? {}".format(a, a.isalnum()))





